"""### Model 1 — SARIMA (Statistical Model)

We'll use a Seasonal Autoregressive Integrated Moving Average (SARIMA) model. Before fitting, we need to determine its parameters: `(p, d, q)` for the non-seasonal part and `(P, D, Q, m)` for the seasonal part.

*   **`d` (differencing order):** From our ADF test in Task 2, we found `monthly_sales_ts` to be stationary, so we can set `d=0`.
*   **`m` (seasonal period):** For monthly data, `m=12`.

To determine `p, q, P, Q`, we typically examine the Autocorrelation Function (ACF) and Partial Autocorrelation Function (PACF) plots of the differenced series (or the original if stationary). Let's plot them.
"""

import statsmodels.api as sm
import matplotlib.pyplot as plt
import seaborn as sns

# Plot ACF and PACF to help determine (p,q) and (P,Q)
# Adjust lags to be less than len(train_data) // 2, which is 36 // 2 = 18. Using 17.
fig, axes = plt.subplots(2, 1, figsize=(12, 8))
