"""#### Possible Real-World Explanations for Isolation Forest Anomalies

Based on the plot, we can observe that most anomalies tend to occur towards the end of the year, particularly in November and December. This is a common pattern for retail sales due to:

*   **November (High Spike):** This period often includes major shopping holidays like Black Friday and Cyber Monday, leading to significantly higher sales volumes. Back-to-school promotions or early holiday shopping could also contribute.
*   **December (High Spike):** Christmas and year-end sales events drive substantial revenue. Other promotions or clearances might also occur.
*   **Early Year Dips (Low Spike):** Sometimes, a very low sales week might be flagged if it deviates significantly from the typical low-sales period in early January or February, after the holiday rush.

### Method 2: Z-Score Based Anomaly Detection

Z-score based detection is a statistical approach where a data point is considered an anomaly if its Z-score (number of standard deviations from the mean) exceeds a certain threshold. For time series, a rolling Z-score is often used to account for changes in the mean and standard deviation over time. We will flag any week where sales deviate more than 2 standard deviations from the rolling mean.
"""

# Define rolling window size (e.g., 4 weeks for a monthly perspective, or 12 for yearly seasonality context)
window_size = 4

# Calculate rolling mean and standard deviation of 'Weekly Sales'
weekly_sales['rolling_mean'] = weekly_sales['Weekly Sales'].rolling(window=window_size, center=True).mean()
weekly_sales['rolling_std'] = weekly_sales['Weekly Sales'].rolling(window=window_size, center=True).std()

# Calculate Z-score
weekly_sales['z_score'] = (weekly_sales['Weekly Sales'] - weekly_sales['rolling_mean']) / weekly_sales['rolling_std']

# Define anomaly threshold (e.g., 2 standard deviations)
threshold = 2

# Flag anomalies based on Z-score
weekly_sales['z_score_anomaly'] = weekly_sales['z_score'].apply(lambda x: -1 if np.abs(x) > threshold else 1)

# Filter for anomalies
z_score_anomalies = weekly_sales[weekly_sales['z_score_anomaly'] == -1]

print('Detected Anomalies using Z-Score (rolling window = {}):'.format(window_size))
display(z_score_anomalies[['Order Date', 'Weekly Sales', 'z_score']])

"""#### Visualization of Z-Score Anomalies

Let's visualize the Z-Score detected anomalies on the sales time series plot.
"""

