plt.fill_between(weekly_sales['Order Date'],
                 weekly_sales['rolling_mean'] - threshold * weekly_sales['rolling_std'],
                 weekly_sales['rolling_mean'] + threshold * weekly_sales['rolling_std'],
                 color='lightgreen', alpha=0.3, label=f'Mean +/- {threshold} Std Dev')

plt.scatter(z_score_anomalies['Order Date'], z_score_anomalies['Weekly Sales'],
            color='purple', label='Z-Score Anomaly', marker='x', s=100, zorder=5)

