# Helper to suppress Prophet output, as we will be training multiple models
@contextlib.contextmanager
def suppress_stdout_stderr():
    """A context manager that redirects stdout and stderr to devnull."""
    with open(os.devnull, 'w') as fnull:
        old_stdout = sys.stdout
        old_stderr = sys.stderr
        sys.stdout = fnull
        sys.stderr = fnull
        try:
            yield
        finally:
            sys.stdout = old_stdout
            sys.stderr = old_stderr

"""### Step 2: Define `forecast_segment_with_prophet` Function

This is the core function that encapsulates the logic for forecasting a single segment. It takes the main dataframe, a column to filter by (e.g., 'Category'), the specific value within that column (e.g., 'Furniture'), and the number of periods to forecast.

Inside the function:
1.  **Filter Data**: It first filters the global `df` for the specified `filter_val` (e.g., all rows where 'Category' is 'Furniture').
2.  **Aggregate Monthly Sales**: It then aggregates these filtered daily sales into monthly totals, similar to how we prepared `monthly_sales_ts`. The columns are renamed to `ds` and `y` as required by Prophet.
3.  **Data Check**: It checks if there's enough data for Prophet to train. If not, it skips the segment.
4.  **Initialize Prophet**: A Prophet model is initialized with `additive` seasonality and `yearly_seasonality=True`, mirroring the configuration of our best overall Prophet model.
5.  **Fit Model (Suppressed Output)**: The model is fitted to the segment's monthly sales data. The `suppress_stdout_stderr()` context manager prevents Prophet from printing its fitting messages repeatedly.
6.  **Generate Future Dates**: It creates a dataframe for the future dates we want to forecast (3 months in this case).
7.  **Predict**: The model predicts sales for these future dates.
8.  **Return Forecast**: It returns a DataFrame containing the `ds` (date) and `yhat` (predicted sales) for the segment.
"""

# 1. Define a function to prepare data, train Prophet, and forecast for a segment
def forecast_segment_with_prophet(data_df, filter_col, filter_val, periods=3, freq='ME'):
    """
    Filters the main DataFrame by a segment, aggregates monthly sales,
    trains a Prophet model, and generates a future forecast.

    Args:
        data_df (pd.DataFrame): The original DataFrame with 'Order Date', 'Category'/'Region', 'Sales'.
        filter_col (str): The column to filter by (e.g., 'Category' or 'Region').
        filter_val (str): The value to filter for (e.g., 'Furniture', 'West').
        periods (int): Number of future periods to forecast.
        freq (str): Frequency of the forecast (e.g., 'ME' for month end).

    Returns:
        pd.DataFrame: A DataFrame containing the future forecast for the segment, indexed by date.
    """
    # Filter data for the specific segment
    segment_df = data_df[data_df[filter_col] == filter_val].copy()

    # Aggregate daily sales into monthly totals for the segment
    segment_monthly_sales = segment_df.set_index('Order Date').resample('ME')['Sales'].sum().reset_index()
    segment_monthly_sales.rename(columns={'Sales': 'y', 'Order Date': 'ds'}, inplace=True)

    # Prophet requires at least 2 data points for yearly seasonality. Check if enough data exists.
    if len(segment_monthly_sales) < 2:
        print(f"Skipping {filter_val} due to insufficient data for Prophet.")
        return pd.DataFrame()

    # Initialize Prophet model (using same config as the best overall Prophet model)
    model = Prophet(
        seasonality_mode='additive',
        yearly_seasonality=True,
        changepoint_prior_scale=0.05
    )

    # Fit the model on the segment's data, suppressing verbose output
    with suppress_stdout_stderr():
        model.fit(segment_monthly_sales)

    # Create a future dataframe for forecasting
    future = model.make_future_dataframe(periods=periods, freq=freq, include_history=False)

    # Generate forecast
    forecast = model.predict(future)

    # Extract relevant forecast columns and return
    return forecast[['ds', 'yhat']].set_index('ds')

