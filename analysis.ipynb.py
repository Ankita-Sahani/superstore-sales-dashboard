print('ADF Statistic: %f' % result[0])
print('p-value: %f' % result[1])
print('Critical Values:')
for key, value in result[4].items():
    print('\t%s: %.3f' % (key, value))

if result[1] <= 0.05:
    print("\nInterpretation: The p-value is less than 0.05, so we reject the null hypothesis. The time series is stationary.")
else:
    print("\nInterpretation: The p-value is greater than 0.05, so we fail to reject the null hypothesis. The time series is non-stationary. Differencing is likely needed.")

