plt.tight_layout(rect=[0, 0, 1, 0.96]) # Adjust layout to prevent title overlap
plt.show()

"""**Interpreting ACF/PACF Plots for SARIMA Parameters:**

*   **Non-seasonal (p, q):**
    *   `p` (AR order): Look at the PACF plot. A significant spike at lag `k` and then a drop to insignificance often suggests `p=k`.
    *   `q` (MA order): Look at the ACF plot. A significant spike at lag `k` and then a drop to insignificance often suggests `q=k`.
    *   *Observation from plots:* There are no clear strong non-seasonal spikes after lag 1 or 2 in both ACF and PACF that sharply cut off. We can consider `p=1` or `q=1` if it improves the model. For a more parsimonious model, `p=0, q=0` could be a starting point if seasonal components explain most variance.

*   **Seasonal (P, D, Q, m=12):**
    *   `P` (Seasonal AR order): Look at significant spikes at seasonal lags (12, 24, ...) in the PACF plot.
    *   `Q` (Seasonal MA order): Look at significant spikes at seasonal lags (12, 24, ...) in the ACF plot.
    *   `D` (Seasonal differencing order): We already established `D=0` as the series is stationary.
    *   *Observation from plots:* We see a strong spike at lag 12 in both ACF and PACF, and then another one at lag 24 in PACF. This strongly suggests seasonal components.
        *   The ACF drops off slowly at seasonal lags, suggesting a seasonal MA component (`Q`).
        *   The PACF has a clear spike at lag 12, suggesting a seasonal AR component (`P`).

Given these observations, a reasonable starting point for our SARIMA model parameters, aiming for parsimony and effectiveness, could be:

*   **Non-seasonal order `(p, d, q)`: `(0, 0, 0)`** (assuming non-seasonal effects are minor compared to seasonality or are captured by seasonal terms)
*   **Seasonal order `(P, D, Q, m)`: `(1, 0, 1, 12)`** (due to strong spikes at seasonal lags in both ACF and PACF, suggesting both seasonal AR and MA components, with D=0 for stationarity and m=12 for monthly data).

Let's fit the SARIMA model with these parameters.
"""

