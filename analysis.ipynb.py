"""### Step 3: Apply K-Means Clustering and Visualize Clusters with PCA

After determining the optimal number of clusters (let's assume 3 for now, and then compare with 4), we will apply K-Means to the scaled data. To visualize the clusters, we will use Principal Component Analysis (PCA) to reduce the dimensionality of our features to two components.
"""

from sklearn.decomposition import PCA
import matplotlib.pyplot as plt
import seaborn as sns

def apply_kmeans_and_visualize(X_scaled_df, n_clusters, title_suffix, save_fig_name=None):
    # Apply K-Means clustering
    kmeans = KMeans(n_clusters=n_clusters, random_state=42, n_init=10)
    kmeans.fit(X_scaled_df)
    clusters = kmeans.labels_
    segmentation_df[f'Cluster_{n_clusters}'] = clusters

    print(f"\nK-Means Clustering with {n_clusters} Clusters ({title_suffix}):")
    display(segmentation_df[[f'Cluster_{n_clusters}']].value_counts().sort_index())

    # Reduce dimensions to 2 using PCA for visualization
    pca = PCA(n_components=2)
    components = pca.fit_transform(X_scaled_df)
    pca_df = pd.DataFrame(data=components, columns=['PC1', 'PC2'], index=X_scaled_df.index)
    pca_df[f'Cluster_{n_clusters}'] = clusters

    # Plot the clusters
    plt.figure(figsize=(10, 8))
    sns.scatterplot(
        x='PC1', y='PC2', hue=f'Cluster_{n_clusters}', data=pca_df,
        palette=sns.color_palette('tab10', n_clusters),
        legend='full', alpha=0.8
    )
    plt.title(f'Product Sub-Category Clusters (K={n_clusters}) - {title_suffix}')
    plt.xlabel(f'Principal Component 1 ({pca.explained_variance_ratio_[0]*100:.2f}%)')
    plt.ylabel(f'Principal Component 2 ({pca.explained_variance_ratio_[1]*100:.2f}%)')
    plt.grid(True)
    if save_fig_name:
        plt.savefig(save_fig_name) # Save the cluster chart
    plt.show()

    # Display cluster centers (inverse transformed to original scale for interpretation)
    cluster_centers_scaled = kmeans.cluster_centers_
    cluster_centers_original_scale = scaler.inverse_transform(cluster_centers_scaled)
    cluster_centers_df = pd.DataFrame(cluster_centers_original_scale, columns=X_scaled_df.columns)
    cluster_centers_df.index.name = 'Cluster'
    print(f"\nCluster Centers (K={n_clusters}) - {title_suffix} (Original Scale):")
    display(cluster_centers_df.round(2))

# Apply K-Means with 3 clusters
apply_kmeans_and_visualize(X_scaled_df, 3, 'Initial run with k=3')

