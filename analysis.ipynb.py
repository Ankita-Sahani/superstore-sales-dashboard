# Function to generate future features and make iterative predictions
def create_and_predict_future_xgb(model, historical_df, periods=3, freq='ME'):
    future_forecasts = []
    current_df = historical_df.copy() # Use a copy to append forecasts

    last_date = current_df.index[-1]
    future_dates = pd.date_range(start=last_date, periods=periods + 1, freq=freq)[1:]

    for i in range(periods):
        # Get features for the next prediction step
        current_future_date = future_dates[i]

        # Extract lag features from the current_df (which includes actuals and previous forecasts)
        lag_1 = current_df['target_sales'].iloc[-1]
        lag_2 = current_df['target_sales'].iloc[-2]
        lag_3 = current_df['target_sales'].iloc[-3]

        # Rolling mean calculation
