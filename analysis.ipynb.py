"""## Task 5 — Anomaly Detection in Sales Data

We will now move to anomaly detection in our weekly sales data. Anomalies can represent unusual events that significantly deviate from the expected pattern, such as sudden spikes due to promotional events or unusual dips due to unforeseen circumstances.

We will use two distinct methods:
1.  **Isolation Forest:** A machine learning algorithm often used for anomaly detection, especially in datasets with multiple features.
2.  **Z-Score based detection:** A statistical method that identifies data points that are a certain number of standard deviations away from the mean.

### Method 1: Isolation Forest for Anomaly Detection

Isolation Forest works by building an ensemble of isolation trees. The principle is that anomalies are 'few and different' and therefore are susceptible to isolation. Anomalies are easier to isolate than normal data points because they are much fewer and have attribute values that are different from normal instances. This algorithm explicitly tries to isolate anomalous data points, rather than building models of 'normal' data.
"""

from sklearn.ensemble import IsolationForest
import matplotlib.pyplot as plt
import numpy as np

# Prepare data for Isolation Forest
# We'll use 'Weekly Sales' as the feature for anomaly detection
# Convert to a 2D array as required by IsolationForest
X_if = weekly_sales[['Weekly Sales']].copy()

# Initialize and train Isolation Forest model
# contamination: The proportion of outliers in the data set.
# This is a critical parameter. We'll start with an estimate (e.g., 0.01 for 1% outliers).
model_if = IsolationForest(random_state=42, contamination=0.01)
model_if.fit(X_if)

# Predict anomalies (-1 for outliers, 1 for inliers)
anomaly_predictions_if = model_if.predict(X_if)
anomaly_scores_if = model_if.decision_function(X_if)

# Add anomaly predictions and scores to the weekly_sales DataFrame
weekly_sales['isolation_forest_anomaly'] = anomaly_predictions_if
weekly_sales['isolation_forest_score'] = anomaly_scores_if

# Filter for anomalies
isolation_forest_anomalies = weekly_sales[weekly_sales['isolation_forest_anomaly'] == -1]

print('Detected Anomalies using Isolation Forest:')
display(isolation_forest_anomalies[['Order Date', 'Weekly Sales', 'isolation_forest_score']])

"""#### Visualization of Isolation Forest Anomalies

Let's visualize these detected anomalies on a time series plot to see their positions relative to the overall sales trend.
"""

plt.figure(figsize=(16, 8))
plt.plot(weekly_sales['Order Date'], weekly_sales['Weekly Sales'], label='Weekly Sales', color='blue')
plt.scatter(isolation_forest_anomalies['Order Date'], isolation_forest_anomalies['Weekly Sales'],
            color='red', label='Isolation Forest Anomaly', marker='o', s=100, zorder=5)

